<?php
$a = 10;
$b = 20;

echo ($a == $b); // equal
echo "<br>";

echo ($a != $b); // not equal
echo "<br>";

echo ($a > $b); // greater than
echo "<br>";

echo ($a < $b); // less than
echo "<br>";

echo ($a >= $b); // greater than or equal
echo "<br>";

echo ($a <= $b); // less than or equal
echo "<br>";

echo ($a <=> $b); // spaceship operator
?>

